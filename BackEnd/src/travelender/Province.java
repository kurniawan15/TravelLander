package travelender;

/**
 *
 * @author Dewi Roaza
 */
public class Province {
    private String idProv;
    private String nameProv;
    private String ibuKota;

    /**
     * @return the idProv
     */
    public String getIdProv() {
        return idProv;
    }

    /**
     * @param idProv the idProv to set
     */
    public void setIdProv(String idProv) {
        this.idProv = idProv;
    }

    /**
     * @return the nameProv
     */
    public String getNameProv() {
        return nameProv;
    }

    /**
     * @param nameProv the nameProv to set
     */
    public void setNameProv(String nameProv) {
        this.nameProv = nameProv;
    }

    /**
     * @return the ibuKota
     */
    public String getIbuKota() {
        return ibuKota;
    }

    /**
     * @param ibuKota the ibuKota to set
     */
    public void setIbuKota(String ibuKota) {
        this.ibuKota = ibuKota;
    }
}
