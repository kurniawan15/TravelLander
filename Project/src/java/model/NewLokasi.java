package model;

/**
 *
 * @author Dewi Roaza
 */
public class NewLokasi {
    private String idLokasi;
    private String namaLokasi;
    private String latitude;
    private String longitute;

    /**
     * @return the idLokasi
     */
    public String getIdLokasi() {
        return idLokasi;
    }

    /**
     * @param idLokasi the idLokasi to set
     */
    public void setIdLokasi(String idLokasi) {
        this.idLokasi = idLokasi;
    }

    /**
     * @return the namaLokasi
     */
    public String getNamaLokasi() {
        return namaLokasi;
    }

    /**
     * @param namaLokasi the namaLokasi to set
     */
    public void setNamaLokasi(String namaLokasi) {
        this.namaLokasi = namaLokasi;
    }

    /**
     * @return the latitude
     */
    public String getLatitude() {
        return latitude;
    }

    /**
     * @param latitude the latitude to set
     */
    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    /**
     * @return the longitute
     */
    public String getLongitute() {
        return longitute;
    }

    /**
     * @param longitute the longitute to set
     */
    public void setLongitute(String longitute) {
        this.longitute = longitute;
    }
}
