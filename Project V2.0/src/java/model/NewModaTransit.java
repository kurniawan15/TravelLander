public class NewModaTransit {
    public String kdEvent;
    public String vehicleMode;
    public String transit;

    /**
     * @return the kdEvent
     */
    public String getKdEvent() {
        return kdEvent;
    }

    /**
     * @param kdEvent the kdEvent to set
     */
    public void setKdEvent(String kdEvent) {
        this.kdEvent = kdEvent;
    }

    /**
     * @return the vehicleMode
     */
    public String getVehicleMode() {
        return vehicleMode;
    }

    /**
     * @param vehicleMode the vehicleMode to set
     */
    public void setVehicleMode(String vehicleMode) {
        this.vehicleMode = vehicleMode;
    }

    /**
     * @return the transit
     */
    public String getTransit() {
        return transit;
    }

    /**
     * @param transit the transit to set
     */
    public void setTransit(String transit) {
        this.transit = transit;
    }
}
