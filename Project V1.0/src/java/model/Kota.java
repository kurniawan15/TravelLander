package model;
/**
 *
 * @author Dewi Roaza
 */
public class Kota {
    public String idKota;
    public String namaKota;
    public String idProvinsi;

    /**
     * @return the idKota
     */
    public String getIdKota() {
        return idKota;
    }

    /**
     * @param idKota the idKota to set
     */
    public void setIdKota(String idKota) {
        this.idKota = idKota;
    }

    /**
     * @return the namaKota
     */
    public String getNamaKota() {
        return namaKota;
    }

    /**
     * @param namaKota the namaKota to set
     */
    public void setNamaKota(String namaKota) {
        this.namaKota = namaKota;
    }

    /**
     * @return the idProvinsi
     */
    public String getIdProvinsi() {
        return idProvinsi;
    }

    /**
     * @param idProvinsi the idProvinsi to set
     */
    public void setIdProvinsi(String idProvinsi) {
        this.idProvinsi = idProvinsi;
    }

    
}
